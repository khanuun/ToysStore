package com.example.toystore.model;

public class Mainan {
    private String id_mainan;
    private String nama_mainan;
    private String harga_mainan;

    public String getId_mainan() {
        return id_mainan;
    }

    public void setId_mainan(String id_mainan) {
        this.id_mainan = id_mainan;
    }

    public String getNama_mainan() {
        return nama_mainan;
    }

    public void setNama_mainan(String nama_mainan) {
        this.nama_mainan = nama_mainan;
    }

    public String getHarga_mainan() {
        return harga_mainan;
    }

    public void setHarga_mainan(String harga_mainan) {
        this.harga_mainan = harga_mainan;
    }
}
